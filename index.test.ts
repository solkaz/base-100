import { decode, encode } from "./index";

describe("base-100", () => {
  describe("encode", () => {
    test("encodes blank string", () => {
      expect(encode("d")).toBe(Buffer.from([240, 159, 143, 183]).toString());
    });
    test("encodes", () => {
      expect(encode("the quick brown fox jumped over the lazy dog")).toBe(
        `??????????????????????????????????????????????????????????????????????????????????????????`
      );
    });
  });

  describe("decode", () => {
    test.skip("decodes classic example", () => {
      expect(
        decode(
          `??????????????????????????????????????????????????????????????????????????????????????????`
        )
      ).toBe("the quick brown fox jumped over the lazy dog");
    });
  });
});
